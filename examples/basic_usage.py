"""
FastPDF Basic Usage Examples
============================

Run: python examples/basic_usage.py
"""

from fastpdf import render_pdf, render_pdf_to_file, RenderOptions, PdfEngine


def example_simple():
    """Simplest possible PDF generation."""
    pdf_bytes = render_pdf("<h1>Hello, FastPDF!</h1>")
    with open("output/simple.pdf", "wb") as f:
        f.write(pdf_bytes)
    print(f"✓ Simple PDF: {len(pdf_bytes)} bytes")


def example_styled():
    """PDF with inline CSS styling."""
    html = """
    <html>
    <head>
        <style>
            body { font-family: Helvetica, sans-serif; color: #333; }
            h1 { color: #1a56db; border-bottom: 2px solid #1a56db; padding-bottom: 10px; }
            .content { margin: 20px 0; line-height: 1.6; }
            .highlight { background-color: #fef3c7; padding: 2px 6px; }
            table { width: 100%; border-collapse: collapse; margin-top: 20px; }
            th { background-color: #1a56db; color: white; padding: 12px; text-align: left; }
            td { padding: 10px 12px; border-bottom: 1px solid #e5e7eb; }
            tr:nth-child(even) { background-color: #f9fafb; }
        </style>
    </head>
    <body>
        <h1>Styled Document</h1>
        <div class="content">
            <p>This is a <span class="highlight">styled PDF</span> generated with FastPDF.</p>
            <p>It supports full CSS styling including colors, fonts, tables, and more.</p>
        </div>
        <table>
            <thead>
                <tr><th>Feature</th><th>Status</th><th>Performance</th></tr>
            </thead>
            <tbody>
                <tr><td>HTML Parsing</td><td>✓ Complete</td><td>&lt;1ms</td></tr>
                <tr><td>CSS Styling</td><td>✓ Complete</td><td>&lt;2ms</td></tr>
                <tr><td>Layout Engine</td><td>✓ Complete</td><td>&lt;5ms</td></tr>
                <tr><td>PDF Generation</td><td>✓ Complete</td><td>&lt;10ms</td></tr>
            </tbody>
        </table>
    </body>
    </html>
    """
