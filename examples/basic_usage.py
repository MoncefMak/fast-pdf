def example_batch():
    """Parallel batch rendering."""
    items = [
        {"html": f"<h1>Invoice #{i}</h1><p>Amount: ${i * 100:.2f}</p>"}
        for i in range(1, 11)
    ]
    
    from fastpdf import batch_render
    results = batch_render(items)
    
    for i, pdf_bytes in enumerate(results, 1):
        with open(f"output/invoice_{i}.pdf", "wb") as f:
            f.write(pdf_bytes)
    
    print(f"✓ {len(results)} invoices rendered in parallel")


